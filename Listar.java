    void listar() {
        ArrayList<Libro> libros = archivo.leerArchivo();
        String columna[] = {"Código", "Título", "Autor", "País", "Año"};
        int[] ancho = {80, 385, 175, 90, 50};
        
        DefaultTableModel mt = new DefaultTableModel(null, columna){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }        
        };
        
        tblRegistro.setModel(mt);
        
        for(int i = 0; i < tblRegistro.getColumnCount(); i++){
            tblRegistro.getColumnModel().getColumn(i).setPreferredWidth(ancho[i]);
        }
        
        for(Libro x: libros){
            String cod = String.format("%010d", x.getCodigo());
            mt.addRow(new Object[]{cod, x.getTitulo(), x.getAutor(), x.getPais(), x.getAño()});
        }
    }
    
    void seleccionar(int fila, MouseEvent evt) {
        int cod = Integer.parseInt(tblRegistro.getValueAt(fila, 0).toString());

        if(evt.getButton() == MouseEvent.BUTTON1){
            VerLibro ver = new VerLibro(new javax.swing.JFrame(), true);
            ver.asignarArchivo(archivo);
            ver.recibirDatos(cod);
            ver.setVisible(true);
        } else if(evt.getButton() == MouseEvent.BUTTON3){
            int y = evt.getY();
            
            if((y >= fila * 16) && (y <= (fila * 16) + 16)){
                pMnModificarEliminar.show(this, 150, (fila * 16) + 19);
            }
        }
    }

