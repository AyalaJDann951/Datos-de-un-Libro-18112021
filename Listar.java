    private void mnItModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnItModificarActionPerformed
        ArrayList<Libro> libros = archivo.leerArchivo();
        int fila = tblRegistro.getSelectedRow();
        int cod = Integer.parseInt(tblRegistro.getValueAt(fila, 0).toString());
        
        Modificar modificar = new Modificar(new javax.swing.JFrame(), true);
        modificar.asignarArchivo(archivo);
        modificar.codigo(cod);
        modificar.recibirDatos();
        modificar.setVisible(true);
        listar();
    }//GEN-LAST:event_mnItModificarActionPerformed

    private void mnItEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnItEliminarActionPerformed
        ArrayList<Libro> libros = archivo.leerArchivo();
        int fila = tblRegistro.getSelectedRow();
        int cod = Integer.parseInt(tblRegistro.getValueAt(fila, 0).toString());
        
        for(Libro x: libros){
            if(cod == x.getCodigo()) archivo.eliminarArchivo(cod, x.getTitulo());
        }
        
        listar();
    }//GEN-LAST:event_mnItEliminarActionPerformed

